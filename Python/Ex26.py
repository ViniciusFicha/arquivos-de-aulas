# Exercícios
# Crie funções que duplicam, triplicam e quadruplicam
# o número recebido como parâmetro.

def duplicador(n):
    return n * 2
def triplicador(n):
    return n * 3
def quadruplicador(n):
    return n * 4

print(duplicador(10))
print(triplicador(100))   
print(quadruplicador(1000))
